package chillOut;
class Ifelseif {
	public static void main(String[] args) 
	{
int marks=55;
if(marks > 85)
{
	System.out.println("A grade");
}
else if(marks >70)
{
	System.out.println("B grade");
}
else if(marks >55)
{
	System.out.println("C grade");
}
else if(marks >45)
{
System.out.println("D grade");
}
else if(marks >35)
{
System.out.println("E grade");
}
else
{
	System.out.println("Fail");
}
}
}
